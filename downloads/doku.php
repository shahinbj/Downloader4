<h2 class="sectionedit4" id="development_sources">Development Sources</h2>
<div class="level2">

<p>
You&#039;ll need subversion to get them. <br/>

Latest development sources can be found at <a href="http://github.com/aircrack-ng/aircrack-ng" class="urlextern" title="http://github.com/aircrack-ng/aircrack-ng" rel="ugc nofollow">github.com/aircrack-ng/aircrack-ng</a>. <br/>

<br/>

By typing the following command in a console, you&#039;ll check them out in <strong>aircrack-ng</strong> directory:
</p>
<pre class="code">git clone http://github.com/aircrack-ng/aircrack-ng</pre>

<p>
On windows, you can use <a href="https://tortoisegit.org/" class="urlextern" title="https://tortoisegit.org/" rel="ugc nofollow">TortoiseGit</a> to get them.
</p>

